    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int mct = Integer.parseInt(tab3cb1.getSelectedItem().toString());
        int mvt = Integer.parseInt(tab3cb2.getSelectedItem().toString());
        
        CauThu ct = new CauThu();
        ViTri vt = new ViTri();
        for(int i = 0; i < lct.size(); i++) {
            if(lct.get(i).getMact() == mct) {
                ct = lct.get(i);
            }
        }
        for(int i = 0; i < lvt.size(); i++) {
            if(lvt.get(i).getMavitri()== mvt) {
                vt = lvt.get(i);
            }
        }
        
        try { 
            int ngay = Integer.parseInt(tab3tf1.getText());
            int thang = Integer.parseInt(tab3tf2.getText());
            int nam = Integer.parseInt(tab3tf3.getText());
            if(ngay <= 0 || ngay > 31 || thang <= 0 || thang > 12) {
                throw new SoEx();
            }
            
            boolean check = false;
            for(int i = 0; i < lbpc.size(); i++) {
                if(lbpc.get(i).getCt().getMact() == mct && lbpc.get(i).getNgay() == ngay && lbpc.get(i).getThang()== thang && lbpc.get(i).getNam()== nam) {
                    JOptionPane.showMessageDialog(this, "khong duoc choi cung 1 ngay");
                    break;
                }
               
            }
            if(!check) {
                BangPhanCong t = new BangPhanCong(ct, vt, ngay, thang, nam);
                    bangDefaultTableModel.addRow(t.toObject());
                    lbpc.add(t);
            }

        } catch (SoEx ex) {
            JOptionPane.showMessageDialog(this, "vui long nhap dung ngay thang nam!");
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "vui long nhap dung so!!");
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed

