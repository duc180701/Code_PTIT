    private void loadData() {
        File f1 = new File(file1);
        File f2 = new File(file2);
        File f3 = new File(file3);
        tab3cb1.removeAllItems();
                tab3cb2.removeAllItems();


        if(f1.exists()) {
            lct = IOFile.readFile(file1);
            lct.forEach(e -> { 
                cauthuDefaultTableModel.addRow(e.toObject());
                tab3cb1.addItem(String.valueOf(e.getMact()));
            });
        }
        else lct = new ArrayList<>();
        
        if(f2.exists()) {
            lvt = IOFile.readFile(file2);
            lvt.forEach(e -> { 
                vitriDefaultTableModel.addRow(e.toObject());
                tab3cb2.addItem(String.valueOf(e.getMavitri()));
            });
        }
        else lvt = new ArrayList<>();
        
        if(f3.exists()) {
            lbpc = IOFile.readFile(file3);
            lbpc.forEach(e -> { 
                bangDefaultTableModel.addRow(e.toObject());
            });
        }
        else lbpc = new ArrayList<>();
    }
    
    private void write(String path, ArrayList list) {
        cauthuDefaultTableModel.setRowCount(0);
        vitriDefaultTableModel.setRowCount(0);
        bangDefaultTableModel.setRowCount(0);
        IOFile.writeFile(path, list);
        loadData();
        JOptionPane.showMessageDialog(this, "da luu vao file!");
    }

